import numpy as np
import math

def compute_mst_weight(inst):
    dist = get_distance_matrix(inst)
    n = dist.shape[0]

    in_mst = [False] * n
    min_edge = [math.inf] * n

    # Start from node 0
    min_edge[0] = 0.0
    total_weight = 0.0

    for _ in range(n):
        # Pick the node not yet in MST with smallest connecting edge
        u = -1
        best = math.inf
        for i in range(n):
            if not in_mst[i] and min_edge[i] < best:
                best = min_edge[i]
                u = i

        # Add it to MST
        in_mst[u] = True
        total_weight += best

        # Update neighbors
        for v in range(n):
            if not in_mst[v] and dist[u, v] < min_edge[v]:
                min_edge[v] = dist[u, v]

    return total_weight

