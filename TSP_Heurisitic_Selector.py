    dist = get_distance_matrix(inst)
    n = dist.shape[0]

    # First pass: mean
    total = 0.0
    count = 0
    for i in range(n):
        for j in range(i + 1, n):
            total += dist[i, j]
            count += 1

    mean = total / count

    # Second pass: std
    var = 0.0
    for i in range(n):
        for j in range(i + 1, n):
            diff = dist[i, j] - mean
            var += diff * diff

    var /= count
    std = math.sqrt(var)

    if std == 0:
        return 0.0

