def three_opt_swap(tour, i, j, k, case):
    A = tour[:i]
    B = tour[i:j]
    C = tour[j:k]
    D = tour[k:]

    if case == 1:
        return A + B[::-1] + C + D
    elif case == 2:
        return A + B + C[::-1] + D
    elif case == 3:
        return A + B[::-1] + C[::-1] + D
    elif case == 4:
        return A + C + B + D
    elif case == 5:
        return A + C[::-1] + B + D

