"""Greedy Algorithm"""

def greedy_tsp_from_matrix(dist):
    """
    Greedy (cheapest-link) TSP heuristic.
    dist: NxN symmetric distance matrix
    returns: tour, cost
    """
    n = len(dist)

    # ---------- Union-Find ----------
    parent = list(range(n))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(x, y):
        rx, ry = find(x), find(y)
        if rx != ry:
            parent[ry] = rx

    # ---------- Build and sort all edges ----------
    edges = []
    for i in range(n):
        for j in range(i + 1, n):
            edges.append((dist[i][j], i, j))

    edges.sort()

    degree = [0] * n
    chosen_edges = []

    # ---------- Greedy edge selection ----------
    for w, u, v in edges:
        if degree[u] == 2 or degree[v] == 2:
            continue

        # Check for subtour
        if find(u) == find(v):
            # Allow only if this is the final edge
            if len(chosen_edges) == n - 1:
                chosen_edges.append((u, v))
                degree[u] += 1
                degree[v] += 1
            continue

        # Accept edge
        chosen_edges.append((u, v))
        union(u, v)
        degree[u] += 1
        degree[v] += 1

        if len(chosen_edges) == n:
            break

    # ---------- Reconstruct tour ----------
    adj = {i: [] for i in range(n)}
    for u, v in chosen_edges:
        adj[u].append(v)
        adj[v].append(u)

    tour = [0]
    prev = -1
    curr = 0

    while True:
        nxt = adj[curr][0] if adj[curr][0] != prev else adj[curr][1]
        if nxt == 0:
            break
        tour.append(nxt)
        prev, curr = curr, nxt

    tour.append(0)

    cost = sum(dist[tour[i]][tour[i + 1]] for i in range(len(tour) - 1))

    return tour, cost

