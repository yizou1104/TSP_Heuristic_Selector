import random

def compute_triangle_violation_rate(inst, num_samples=200000, seed=42):
    dist = get_distance_matrix(inst)
    n = dist.shape[0]

    if n < 3:
        return 0.0

    random.seed(seed)

    violations = 0
    trials = 0

    for _ in range(num_samples):
        i, j, k = random.sample(range(n), 3)

        dij = dist[i, j]
        djk = dist[j, k]
        dik = dist[i, k]

        # Triangle inequality check
        if dik > dij + djk:
            violations += 1

        trials += 1

